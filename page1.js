function isEven(number) {
    if (number % 2 == 0){
        console.log(`${number} is even`)
    }else {
        console.log(`${number} is not even`)
    }
}

isEven(10)
isEven(55)

function isOdd(number) {
    if (number % 2 != 0){
        console.log(`${number} is odd`)
    }else {
        console.log(`${number} is not odd`)
    }
}

isOdd(11)
isOdd(50)

